    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tblhasil.getModel();
           model.setRowCount(0);
           try {
               String sql = "Select * From tbljenis";
               PreparedStatement st = conn.prepareStatement(sql);
               ResultSet rs = st.executeQuery();
               while(rs.next()){
                   int id = rs.getInt("KodeJenis");
                   String Nama = rs.getString("Jenis");
                   Object[] rowData = {id,Nama};
                   model.addRow(rowData);
                }
                rs.close();
                st.close();
                   

           } catch (Exception e) {
                Logger.getLogger(MenuJenis.class.getName()).log(Level.SEVERE,null,e);
           }
    }

